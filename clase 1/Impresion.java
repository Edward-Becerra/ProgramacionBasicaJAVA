public class Impresion {// inicia clase
    public static void main(String[]args) {// inicia método
        
        String nombre = "Edward";

        System.out.print("Mi nombre es " + nombre);// Sin salto de línea
        System.out.println("Mi nombre es " + nombre); //Con salto de línea
        System.out.println();// solo salto de línea

        System.out.println("Mi nombre es \"" + nombre + "\"");// nombre entre comillas 
    }// termina método
}// termina clase