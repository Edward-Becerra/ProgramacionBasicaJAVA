public class OperadoresCombinados {// inicia la clase
    public static void main(String[]args) {// inicia método main
         
         int x = 5;

         x = x + 3; 

         System.out.println(x); // muestra 5 + 3 

         x += 2; //Esta operación adicionar 2 a la misma variable X
        
         System.out.println(x); // muestra el resultado de 5 + 3 y adiciona 2


    }// termina método main
}//termina la clase